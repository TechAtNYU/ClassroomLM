import type { Metadata } from "next";
import { Geist, Geist_Mono } from "next/font/google";
import "./globals.css";
import { Toaster } from "@/components/ui/toaster";

import {
  SidebarInset,
  SidebarProvider,
  SidebarTrigger,
} from "@/components/ui/sidebar";
import { ThemeProvider } from "@/components/ui/theme-provider";
import { AppSidebar } from "@/components/ui/sidebar/app-sidebar";
import UserContextProvider from "./lib/userContext/userContext";
import { getUserAndClassroomData } from "./lib/userContext/contextFetcher";
import { cn } from "@/lib/utils";
import { Separator } from "@/components/ui/separator";

const geistSans = Geist({
  variable: "--font-geist-sans",
  subsets: ["latin"],
});

const geistMono = Geist_Mono({
  variable: "--font-geist-mono",
  subsets: ["latin"],
});

export const metadata: Metadata = {
  title: "ClassroomLM",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const userData = await getUserAndClassroomData();
  // TODO: change this?

  return (
    <html lang="en" suppressHydrationWarning>
      <body
        className={cn(
          `font-sans antialiased`,
          geistSans.variable,
          geistMono.variable
        )}
      >
        <ThemeProvider
          attribute="class"
          defaultTheme="system"
          enableSystem
          // disableTransitionOnChange
        >
          {userData ? (
            <SidebarProvider>
              <UserContextProvider userAndClassDataInitial={userData}>
                <AppSidebar />
                <SidebarInset className="md:peer-data-[variant=inset]:mt-5">
                  <main>
                    <header className="flex h-16 shrink-0 items-center gap-2">
                      <div className="flex items-center gap-2 px-4">
                        <SidebarTrigger className="-ml-1" />
                        <Separator
                          orientation="vertical"
                          className="mr-2 h-4"
                        />
                      </div>
                    </header>
                    <div className="flex flex-1 flex-col gap-4 p-4 pt-0">
                      {children}
                      <div className="min-h-[100vh] flex-1 rounded-xl bg-muted/50 md:min-h-min" />
                    </div>
                  </main>
                  <Toaster />
                </SidebarInset>
              </UserContextProvider>
            </SidebarProvider>
          ) : (
            <main>{children}</main>
          )}
        </ThemeProvider>
      </body>
    </html>
  );
}
